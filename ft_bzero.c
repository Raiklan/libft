#include "libft.h"

void	*ft_bzero(void *s, size_t len)
{
	char	*b;
	size_t	i;

	i = 0;
	b = (char*)s;
	while (i < len)
	{
		b[i] = '\0';
		i++;
	}
	return (s);
}
