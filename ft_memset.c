#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*st;
	size_t	i;

	i = 0;
	st = (char*)b;
	while (i < len)
	{
		st[i] = (char)c;
		i++;
	}
	return (b);
}
