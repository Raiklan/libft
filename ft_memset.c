#include "libft.h"

void	*ft_memset(void *s, int c, size_t len)
{
	char	*b;
	size_t	i;

	i = 0;
	b = (char*)s;
	while (i < len)
	{
		b[i] = (char)c;
		i++;
	}
	return (s);
}
