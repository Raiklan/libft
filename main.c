#include <stdio.h>
#include <string.h>
#include <unistd.h>
#include <stdlib.h>

size_t		ft_strlen(const char *str)
{
	size_t i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	size_t	j;
	size_t	i;

	if (!(str = malloc(sizeof(char) * (ft_strlen(s1) + ft_strlen(s2)))))
		return (0);
	i = 0;
	j = 0;
	while (i < ft_strlen(s1))
	{
		str[i] = s1[i];
		i++;
	}
	while (j < ft_strlen(s2))
		str[i++] = s2[j++];
	str[i] = '\0';
	return (str);
}

int main()
{
	char str[] = "lorem ipsum";
	char str2[] = "lala";
	printf("%s\n", ft_strjoin(str, str2));
	return (0);	
}