#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstszie)
{
	size_t	i;

	i = 0;
	if (!(src))
		return (0);
	if (dstszie == 0)
		return (ft_strlen(src));
	while (i < dstszie - 1 && src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (ft_strlen(src));
}
